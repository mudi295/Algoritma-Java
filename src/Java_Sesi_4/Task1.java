package Java_Sesi_4;

public class Task1 {
    public static void main(String[] args) {
        for (int m = 0; m < 3; m++) {
            // Menggunakan loop untuk mencetak angka 1 sampai 6
            for (int h = 1; h <= 6; h++) {
                System.out.print(h + " ");
            }
            // Pindah ke baris berikutnya
            System.out.println();
        }
    }
}